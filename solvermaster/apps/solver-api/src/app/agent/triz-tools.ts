/** Best available name for a TRIZ parameter id: canonical table, else generic. */
function paramName(id: number, provided?: string | null): string {
  const trimmed = provided?.trim();
  if (trimmed) return trimmed;
  return TRIZ_PARAM_NAMES[id] ?? `Parameter ${id}`;
}

